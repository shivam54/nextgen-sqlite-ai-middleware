-- TPC-H Query 4: Order Priority Checking
-- This query counts how many orders were placed in a given quarter
-- where at least one lineitem was received later than its committed date.

SELECT
    o_orderpriority,
    COUNT(*) AS order_count
FROM
    orders
WHERE
    o_orderdate >= date('1993-07-01')
    AND o_orderdate < date('1993-10-01')
    AND EXISTS (
        SELECT *
        FROM lineitem
        WHERE l_orderkey = o_orderkey
        AND l_commitdate < l_receiptdate
    )
GROUP BY
    o_orderpriority
ORDER BY
    o_orderpriority;

